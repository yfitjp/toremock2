      <li><strong>E (Explanation - 説明)</strong>: 提示した証拠や具体例が、どのようにPoint（主張）を支持するのかを詳しく説明します。単に例を挙げるだけでなく、「なぜそれが主張の裏付けになるのか」を論理的に結びつけることが重要です。</li>
