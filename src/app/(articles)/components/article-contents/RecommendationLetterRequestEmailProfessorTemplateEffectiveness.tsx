  <li><strong>推薦状を書いてもらったら必ずお礼を伝える:</strong> 推薦状が無事に提出されたら（または教授から執筆完了の連絡があったら）、速やかに感謝のメールを送りましょう。可能であれば、後日直接お礼に伺うのが最も丁寧です。そして、最終的に進学先が決まった際には、その報告とお礼を改めて伝えることで、良好な関係を維持できます。</li>
