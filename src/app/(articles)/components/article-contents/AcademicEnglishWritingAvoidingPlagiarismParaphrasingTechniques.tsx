      <li><strong>必ず出典を明記する (Citation):</strong> パラフレーズした場合でも、そのアイデアや情報が誰のものであるかを明確に示すために、<strong>必ず出典を記載</strong>しなければなりません。これは学術的な誠実さの基本です。</li>
