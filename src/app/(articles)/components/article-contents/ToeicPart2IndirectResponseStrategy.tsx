<p>TOEIC Part2の間接応答は、一見すると捉えどころがないように思えますが、実はいくつかの典型的なパターンに分類できます。ここでは、特に頻出する7つのパターンを例文と共に解説します。これらのパターンを頭に入れておくだけで、本番での対応力が格段に向上するはずです。</p>
