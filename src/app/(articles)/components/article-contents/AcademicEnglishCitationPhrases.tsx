<p>論文の本文中で正しく引用できたら、最後は<strong style={{ color: '#2563eb' }}>参考文献リスト（References / Bibliography）</strong>の作成です。これは、あなたが論文執筆にあたって参照した全ての文献を、決められた形式に沿ってリストアップするものです。読者があなたの主張の根拠をたどり、さらに深く学ぶための重要な道しるべとなります。</p>
