<h2>なぜ交渉が可能なのか？大学側の事情を理解する</h2>
<p>まず、「本当に大学と学費や奨学金の交渉なんてできるの？」と疑問に思うかもしれません。結論から言うと、<strong>交渉の余地は十分にあります</strong>。大学側にも、学生の交渉に応じる可能性のある、いくつかの理由があるのです。</p>
<p>第一に、大学は<strong>優秀な学生を確保したい</strong>と考えています。合格通知を出したということは、あなたがその大学にとって魅力的な学生であると評価された証です。もし学費がネックで入学を辞退されてしまうと、大学にとっては大きな損失です。特に、あなたが他の大学からも合格を得ている場合、大学はあなたを繋ぎ止めるために、より良い条件を提示しようと考える可能性があります。</p>
<p>第二に、大学、特に私立大学は、ある程度の<strong>予算の柔軟性</strong>を持っている場合があります。奨学金予算は完全に固定されているわけではなく、個別の状況に応じて調整されることがあるのです。大学のウェブサイトのFinancial Aid Office（奨学金オフィス）のセクションをよく読んでみると、「状況の変化に応じてファイナンシャルエイドの見直しを申請できる」といった記述が見つかることもあります。これは、交渉の扉が開かれていることを示唆しています。</p>
<p>また、大学のランキングや評判は、入学する学生の質や数に大きく左右されます。そのため、合格者の入学率（Yield Rate）を高く保つことは、大学にとって非常に重要です。他の競合大学に優秀な学生が流れるのを防ぐためにも、魅力的な奨学金オファーは有効な手段となります。</p>
