  <li><strong>言語交換アプリやサイト</strong>: <strong>Lang-8</strong> (ネイティブスピーカーがお互いの言語の日記を添削し合うコミュニティ) や <strong>HelloTalk</strong> (チャットや通話だけでなく、投稿機能で文章を公開し、添削を受けることも可能) のようなプラットフォームを利用して、ネイティブスピーカーから添削やアドバイスをもらうのも良い方法です。</li>
