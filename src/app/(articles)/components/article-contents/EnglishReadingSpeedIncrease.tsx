    <li><strong>パラグラフの主題文（トピックセンテンス）を見つける:</strong> 英語の論理的な文章では、多くの場合、各パラグラフの最初（または最後）の文がそのパラグラフの主題を述べている「トピックセンテンス」となっています。これを意識的に見つけることで、パラグラフ全体の言いたいことを効率的に把握できます。トピックセンテンスの後は、具体例や理由が続くことが多いです。</li>
