    <ul>
      <li>各ボディパラグラフでは、リーディングの一つの論点と、それに対するレクチャーの具体的な反論・見解をセットで扱います。</li>
      <li>まず、リーディングのポイントを簡潔に述べます。「According to the reading, [リーディングのポイント].」</li>
      <li>次に、レクチャーがそのポイントにどう対応しているかを具体的に説明します。「The lecturer, however, challenges this idea by stating that [レクチャーの反論]. To support this, he/she mentions that [レクチャーの具体例や詳細].」</li>
      <li><strong>接続詞・転換語句の活用:</strong> 論理的な流れを作るために、`However`, `In contrast`, `On the other hand`, `Furthermore`, `Moreover`, `For example`, `Specifically` などの接続詞や転換語句を効果的に使いましょう。これにより、文章がスムーズにつながり、読みやすくなります。</li>
      <li><strong>パラフレーズの徹底:</strong> リーディングやリスニングの言葉をそのままコピーするのではなく、自分の言葉で言い換える（パラフレーズする）ことが非常に重要です。これは語彙力と表現力を示す上で不可欠です。メモに書いたキーワードを元に、異なる単語や文構造で表現する練習をしましょう。</li>
    </ul>

