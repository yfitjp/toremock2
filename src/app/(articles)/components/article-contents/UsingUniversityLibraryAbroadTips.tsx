<p>さらに、論文作成で避けては通れないのが、膨大な量の参考文献の管理です。手作業でリストを作成するのは時間がかかる上に、ミスも起こりがち。そこで絶対に活用したいのが、<span style={{ backgroundColor: '#FEF08A' }}>引用管理ツール（Citation Management Software）</span>です。代表的なものには以下があります。</p>
<ul>
